    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        //aqui va lo del login
        String name = null;
        String pass = null;
         if (this.user.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el nombre de usuario");
            user.requestFocus();//rabFocus();
            return ;
        }
        if (this.password.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la clave");
            password.requestFocus();//rabFocus();
            return ;
        }
        int seleccion=TYPE.getSelectedIndex();
        //System.out.println(seleccion);
        name=this.user.getText();
        pass=this.password.getText();
        
        int verifica = 0;
        
        if(seleccion == 0){
            verifica = Datos.ClsAdmin.veradmin(name,pass);
            if(verifica == 0)
            {    
                JOptionPane.showMessageDialog(rootPane, "Usuario no existe");
                return ; 
            }else{
                //System.out.println(verifica);
                //AlkSystem.setid(verifica);
                PseudoCache.setid(verifica);
                //System.out.println(Integer.parseInt(PseudoCache.getid().trim()));
                
                GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
                GraphicsDevice defaultScreen = ge.getDefaultScreenDevice();
                Rectangle rect = defaultScreen.getDefaultConfiguration().getBounds();
                menu MEN = new menu();
                MEN.setAlwaysOnTop( true );
                MEN.setVisible(true);
                int x = (int) rect.getMaxX() - MEN.getWidth();
                int y = 2;
                System.out.println(x);
                MEN.setLocation(x, y);
                MEN.setBackground(new Color(1.0f,1.0f,1.0f,0.0f));
                this.dispose();
                }
        
        }else if(seleccion == 1){
            verifica = Datos.ClsAdmin.verprofe(name,pass);
            if(verifica == 0)
            {    
                JOptionPane.showMessageDialog(rootPane, "Usuario no existe");
                return ; 
            }else{
                //System.out.println(verifica);
               // menuprof.id = verifica;
                PseudoCache.setid(verifica);
                GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
                GraphicsDevice defaultScreen = ge.getDefaultScreenDevice();
                Rectangle rect = defaultScreen.getDefaultConfiguration().getBounds();
                menuprof MEN = new menuprof();
                MEN.setAlwaysOnTop( true );
                MEN.setVisible(true);
                int x = (int) rect.getMaxX() - MEN.getWidth();
                int y = 2;
                MEN.setLocation(x, y);
                MEN.setBackground(new Color(1.0f,1.0f,1.0f,0.0f));
                this.dispose();
                }
        }else if(seleccion == 2){
                verifica = Datos.ClsAdmin.verest(name,pass);
            if(verifica == 0)
            {    
                JOptionPane.showMessageDialog(rootPane, "Usuario no existe");
                return ; 
            }else{
                //System.out.println(verifica);
               // menuprof.id = verifica;
                PseudoCache.setid(verifica);
                GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
                GraphicsDevice defaultScreen = ge.getDefaultScreenDevice();
                Rectangle rect = defaultScreen.getDefaultConfiguration().getBounds();
                menuest MEN = new menuest();
                MEN.setAlwaysOnTop( true );
                MEN.setVisible(true);
                int x = (int) rect.getMaxX() - MEN.getWidth();
                int y = 2;
                MEN.setLocation(x, y);
                MEN.setBackground(new Color(1.0f,1.0f,1.0f,0.0f));
                this.dispose();
                }
        }
        
    }//GEN-LAST:event_loginActionPerformed

